function p = circle_circle_intersection(c1,r1,c2,r2)
p = zeros(size(c1));
dz = c2 - c1;
d = abs(dz);
a = (r1.*r1-r2.*r2+d.*d)./d/2;
z0 = c1 + dz.*a./d;
h = sqrt(r1.*r1-a.*a);
rz = 1i*dz.*h./d;
p1 = z0 + rz;
p2 = z0 - rz;
e1 = c2-c1;
e2 = p1-c1;
ind = (real(e1).*imag(e2)-imag(e1).*real(e2))>0;
p(ind) = p1(ind);
p(~ind) = p2(~ind);
ind = d>r1+r2 | d<abs(r2-r1);
p(ind) = nan;
